(function($){$(function(){

	
    	var activate = $('.products-nav-row a');
    	
    	$('.products-panel-inner').children().hide();
    	
    	activate.on('click', function(e){
    		
    		e.preventDefault();
    		
    		
    		
    			/* RENAMES CURRENT ID TO ID OF TARGET */
    		
    			var currentId = $(this).attr('id');
    			
    			targetId = currentId.replace('-nav', '');
    			
    			targetId = '#' + targetId;
    			
    			
    		
    		$('.products-panel-inner').children().hide();
    		
    		
         if ($(window).width() >= 751) {
       		
        		var newHeight = $(targetId).outerHeight();
        		
        		$(targetId).find('.product-content-panel').css('height', newHeight);
        		
        		$(targetId).find('.product-content-panel').css('line-height', newHeight + 'px');

        }
    		
    	$(targetId).show('blind');
		
	});

})})(jQuery)
  
